void main() {
  /*
  const: Nunca va a cambiar su estado durante la ejecucion del codigo.
  final: Solo cambia una vez que se le asigna un valor, despues esta ya no puede
  cambiar en ningun momento.
  var: Se utiliza cuando no sabes que tipo de dato vas a recibir.
  Nota: Para imprimir una variable se puede utlizar el simbolo $ para imprimir
  dentro de una comilla simple.
  */

  //String
  var nombre = 'Chabelo';
  String nombreDos = 'Tacho';
  print('Los nombres son: $nombre $nombreDos');

  //Tipos de numeros
  int empleados = 10;
  double salario = 20500.50;
  print('$empleados $salario');
}
